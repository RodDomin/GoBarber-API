import * as Yup from 'yup';
import User from '../models/User';

class UserController {
  async store(req, res) {
    const schema = Yup.object().shape({
      name: Yup.string().required(),
      email: Yup.string()
        .email()
        .required(),
      password: Yup.string()
        .required()
        .min(6),
    });

    if (!(await schema.isValid(req.body))) {
      return res.status(400).json({ error: 'validation failure' });
    }

    const userExists = await User.findOne({ where: { email: req.body.email } });

    if (userExists) {
      return res.status(400).json({ error: 'User already exists' });
    }

    const { id, name, email, provider } = await User.create(req.body);

    return res.json({ id, name, email, provider });
  }

  async update(req, res) {
    const schema = Yup.object().shape({
      name: Yup.string(),
      email: Yup.string().email(),
      oldPassword: Yup.string(),
      password: Yup.string()
        .min(6)
        .when('oldPassword', (oldPassword, field) =>
          oldPassword ? field.required() : field
        ),
    });

    if (!(await schema.isValid(req.body))) {
      return res.status(400).json({ error: 'validation failure' });
    }

    const { email, oldPassword } = req.body;

    const usr = await User.findOne({ where: { email } });

    if (!usr) return res.status(400).json({ error: 'user not found' });

    if (!oldPassword && !(await usr.checkPassword(oldPassword))) {
      return res.status(400).json({ error: 'password does not match' });
    }

    const { id, provider, name } = await usr.update(req.body);

    return res.send({ id, name, email, provider });
  }
}

export default new UserController();
